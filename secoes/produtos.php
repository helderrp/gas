                        <h2>Botijão P13</h2>

                        <img class="img-centered" src="img/portfolio/p1313.jpg" alt="">

                        <p>O botijão de 13kg (P-13) é amplamente utilizado nos lares brasileiros, e neste mercado, o da
                            Liquigás é o mais consumido no país. Ele ‘ganhou’ o nome gás de cozinha porque, de acordo
                            com legislação, só deve ser utilizado em residências.<br>

                            O P-13 apresenta um item de segurança: o plugue fusível. Seu miolo é formado por uma liga de
                            bismuto, estanho e outros sete metais. Se esse material for aquecido por algum agente
                            externo e atingir 70° C, se funde e fica no estado pastoso, escorrendo para dentro do
                            botijão. Isso facilita a saída do gás e evita à possibilidade de explosão.
                            Os consumidores costumam reclamar que o fundo da panela fica preto e injustamente culpam o
                            GLP. Isso acontece quando a mistura do gás com o oxigênio não está adequada. O melhor sinal
                            para perceber isso é a cor da chama, que deve ser azul. Quando há um desequilíbrio, fica
                            amarela e solta uma fumaça preta que origina a chama. Os consumidores devem tomar duas ações
                            nesse caso. A primeira é limpar constantemente os bocais do fogão, e a segunda é trocar a
                            mangueira e o regulador - a recomendação é que a substituição seja feita a cada 5 anos.</p>
